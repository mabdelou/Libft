#include "../../lib/libft.h"

int	ft_lstsize(t_list *lst)
{
	int	a;

	a = 0;
	while (lst != NULL)
	{
		a++;
		lst = lst->next;
	}
	if (lst != NULL)
		a++;
	return (a);
}
